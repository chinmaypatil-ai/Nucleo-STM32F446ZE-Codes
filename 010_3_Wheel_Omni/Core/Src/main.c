	   w = map(w, -127, 127, -50, 50);
	   radian = theta * (3.1415 / 180.0);
	   Vgx = (cos(radian) * Vx) - (sin(radian) * Vy);
	   Vgy = (sin(radian) * Vx) + (cos(radian) * Vy);

