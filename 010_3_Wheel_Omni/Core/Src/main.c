//	   }

	   V1 = velo_const * ((w * r) + Vgx + pidOutput);
	   V2 = velo_const * ((w * r) + (-Vgx / 2) + ((-Vgy) * 1.15470) + pidOutput);
	   V3 = velo_const * ((w * r) + (-Vgx / 2) + ((Vgy)*1.15470) + pidOutput);
//
